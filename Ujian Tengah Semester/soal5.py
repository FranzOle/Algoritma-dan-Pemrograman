# Inisialisasi variabel
a = 2
b = 4
c = 6

print(f"Nilai variabel: a={a}, b={b}, c={c}\n")

# Evaluasi Kondisi Logika:

# 1. a == 4 or b > 2
hasil_1 = a == 4 or b > 2
print(f"1. a == 4 or b > 2 \t\t\t-> {hasil_1}")

# 2. 6 <= c and a > 3
hasil_2 = 6 <= c and a > 3
print(f"2. 6 <= c and a > 3 \t\t\t-> {hasil_2}")

# 3. 1 != b and c != 3 (Mengikuti kondisi tertulis: 1 != b and c != 3)
hasil_3 = 1 != b and c != 3
print(f"3. 1 != b and c != 3 \t\t\t-> {hasil_3}")

# 4. a >= -1 or a <= b
hasil_4 = a >= -1 or a <= b
print(f"4. a >= -1 or a <= b \t\t\t-> {hasil_4}")

# 5. not (a > 2)
hasil_5 = not (a > 2)
print(f"5. not (a > 2) \t\t\t\t-> {hasil_5}")